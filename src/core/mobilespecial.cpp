#include "mobilespecial.h"
#include "dl_strings.h"

const struct spec_type zero_spec_table [] = { { 0, 0 } };

/* the function table */
const   struct  spec_type    *spec_table = zero_spec_table;


/*
 * Given a name, return the appropriate spec fun.
 */
SPEC_FUN *spec_lookup( const char *name )
{
    int i;
    
    if (!name || !name[0])
        return 0;

    for ( i = 0; spec_table[i].name != 0; i++)
        if (!str_prefix( name,spec_table[i].name))
            return spec_table[i].function;

    return 0;
}

const char *spec_name( SPEC_FUN *function)
{
    int i;

    for (i = 0; spec_table[i].function != 0; i++)
        if (function == spec_table[i].function)
            return spec_table[i].name;

    return 0;
}


bool empty_prog( ... )
{
    return false;
}
